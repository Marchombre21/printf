int	ft_isalnum(int c)
{
	unsigned char	d;

	d = (unsigned char)c;
	if ((d <= 90 && d >= 65) || (d <= 122 && d >= 97) || (d >= '0' && d <= '9'))
		return (1);
	else
		return (0);
}
